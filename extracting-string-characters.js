let text = "hello there";
let result = text.charAt(1); //The charAt method returns the character at a specified index in a string.
console.log(result);
let result1 = text.charCodeAt(1); //The charCodeAt method returns the unicode of the character at a specified index in a string.
console.log(result1);
let result2 = text[1];
console.log(result2); //allows property access [ ] on strings
